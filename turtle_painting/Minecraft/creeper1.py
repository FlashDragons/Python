from turtle import *
hideturtle()
# 外框线
penup()
goto(-200, 200)
pendown()
pensize(1)
pencolor("green")
begin_fill()
for i in range(4):
    forward(400)
    right(90)
color("green")
end_fill()
# 右眼
penup()
goto(-150, 150)
pendown()
pencolor("black")
begin_fill()
for i in range(4):
    forward(100)
    right(90)
color("black")
end_fill()
# 左眼
penup()
goto(50, 150)
pendown()
pencolor("black")
begin_fill()
for i in range(4):
    forward(100)
    right(90)
color("black")
end_fill()
# 嘴巴
penup()
goto(-50, 50)
pendown()
pencolor("black")
begin_fill()
forward(100)
right(90)
forward(50)
left(90)
forward(50)
right(90)
forward(150)
right(90)
forward(50)
right(90)
forward(50)
left(90)
forward(100)
left(90)
forward(50)
right(90)
forward(50)
right(90)
forward(150)
right(90)
forward(50)
left(90)
forward(50)
color("black")
end_fill()
done()
